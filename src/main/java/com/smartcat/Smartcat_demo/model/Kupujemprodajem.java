package com.smartcat.Smartcat_demo.model;

public class Kupujemprodajem {
	private String from;
	private String msg;
	
	public Kupujemprodajem() {}
	public Kupujemprodajem(String from, String msg) {
		super();
		this.from = from;
		this.msg = msg;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	

}
